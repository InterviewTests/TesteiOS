import UIKit

extension UIColor {
    struct Default {
        static var red: UIColor {
            return UIColor(red: 226/255, green: 0, blue: 0, alpha: 1)
        }
        
        static var lightRed: UIColor {
            return UIColor(red: 242/255, green: 116/255, blue: 118/255, alpha: 1)
        }
        
        static var darkRed: UIColor {
            return UIColor(red: 207/255, green: 0, blue: 0, alpha: 1)
        }
    }
}
