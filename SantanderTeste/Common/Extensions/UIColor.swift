import UIKit

extension UIColor {
    
    class var customRedOne: UIColor {
        return UIColor(red: 218/255, green: 1/255, blue: 1/255, alpha: 1)
    }
    
    class var customRedTwo: UIColor {
        return UIColor(red: 200/255, green: 1/255, blue: 1/255, alpha: 1)
    }
    
    class var customDarkGray: UIColor {
        return UIColor(white: 0.5, alpha: 1)
    }
    
    class var customLightGray: UIColor {
        return UIColor(white: 0.75, alpha: 1)
    }
}
